    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
     //Salvando Imagem no banco de dados   
        
     switch(res){
         case JFileChooser.CANCEL_OPTION:
             break;
         case JFileChooser.APPROVE_OPTION:
                try {
                    if(!nomeEmCodigo.getText().isEmpty()){
                       if(new Usuario().uploadImagem(ImagemUpload , nomeParaCodificar.getText(),nomeEmCodigo.getText())){
                          //Apagando todo os registros depois de fazer upload
                           painelImagemSelecionar.removeAll(); 
                           painelImagemSelecionar.revalidate();
                           
                           if(!fixarCodigo.isSelected()){
                               nomeParaCodificar.setText("");
                               nomeEmCodigo.setText("");
                           }
                           caminhoImagem.setText(" /...");
                           cardLayout.show(paineis, "painel_Sobre");
                           cardLayout.show(paineis, "painel_Upload");
                           JOptionPane.showMessageDialog(this, "Imagem salva com sucesso!!!");
                       }else{
                           JOptionPane.showMessageDialog(this, "Erro");
                       }
                       //Atualizando a lista de fotos
                       this.listaFotos = SalvarImagemDAO.recuperarImagemIds(Usuario.getId(), Usuario.pasta);
                    }else{
                        JOptionPane.showMessageDialog(this, "Faltando codificação !");
                    }
                   } catch (IOException | SQLException ex ) {
                       Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
                   }
                
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

