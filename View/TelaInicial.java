    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
    //Botão Para Selecionar foto para ser feito o upload    
        //Removendo qualquer foto antes de abrir uma nova.
        painelImagemSelecionar.removeAll();
        //Filtrando extenção de arquivos JPG/PNG.
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagens JPG/PNG", "jpg","png");
        ImagemUpload.addChoosableFileFilter(filtro);
        ImagemUpload.setAcceptAllFileFilterUsed(false);
        //Quardando o resultado da consulta da janela de seleção.
        this.res = ImagemUpload.showOpenDialog(null);
        
        //Verificando oque acontece com o JFikeChooser
        switch(res){
            case JFileChooser.CANCEL_OPTION:  //Caso o usuario cancele.
                break;
            case JFileChooser.APPROVE_OPTION: //Caso o usuario escolha um arquivo
                  //Adicionando a imagem no "painelImagemSelecionar".
                try {
                /**
                 * new Tratamento.Upload().selecionarImagem() Retorna a imagem selecionada ja redimencionada para o Preview_Upload.
                 * new Preview_Upload() Recebe a Imagem e desenha no background.
                 */
                caminhoImagem.setText(ImagemUpload.getCurrentDirectory().toString());
                 painelImagemSelecionar.add(new Preview_Upload(new TratamentoUpload().selecionarImagem(this.ImagemUpload , res))).revalidate();
            
            
                } catch (IOException | SQLException ex) {
                    Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
                }
            case JFileChooser.ERROR_OPTION:
                break;
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

